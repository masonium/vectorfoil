use crate::common::*;

#[derive(Debug, Clone, Copy)]
pub enum EdgeType {
    Visible,
    Invisible,
    Hidden,
    Split, // generated by an internal split
    Culled,
}

/// Internally, all primitive coordinates are kept in 4D as (x/w, y/w,
/// z/w, w).
#[derive(Clone)]
pub enum Primitive {
    /// Triangle
    Triangle {
	points: [Vec4; 3],
	edges: [EdgeType; 3],
    },

    /// Line segment
    Line { points: [Vec4; 2] },

    /// Single point
    Point { point: Vec4 },
}
